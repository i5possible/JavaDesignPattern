package designprinciple;

public class DependencyInversionPrinciple {
    /**
     * The high level should not depend on the low level.
     * And the low level should not depend on the high level.
     * All of them should depend on the interface.
     *
     * For example. When you want to change the database, you just write new implementation of the new database.
     *
     * When you want to migrate a web service to a mobile phone, you just write the user interface without change the
     * backend service.
     */
}
