package designprinciple;

public class OpenClosePrinciple {
    /**
     * A class should be open for extension, but closed for modification.
     */

    /**
     * A class won't be closed at the first time.
     * When change happens, you should find out the area that may be changed in future, and extract them into a class.
     * So you can add new code to deal with the change and you won't need to modify the class any more.
     */
}
